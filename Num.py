# 定义一个整数
my_integer = 10

# 整数运算
sum = my_integer + 5  # 结果为 15
product = my_integer * 3  # 结果为 30

print("Sum:", sum)
print("Product:", product)

# 定义一个浮点数
my_float = 3.14

# 浮点数运算
pi_times_two = my_float * 2  # 结果为 6.28
division = 10 / 3  # 结果为 3.3333333333333335（由于浮点数精度问题）

print("Pi times two:", pi_times_two)
print("Division:", division)

# 定义一个复数
my_complex = 3 + 4j

# 复数运算
conjugate = my_complex.conjugate()  # 求共轭复数，结果为 3-4j
product = my_complex * 2  # 结果为 6+8j

print("Conjugate:", conjugate)
print("Product:", product)